async def run():
    async with AsyncSessionLocal() as db:
        added = skipped = 0

        for src_data in NEW_SOURCES:
            # Vérifier si la source existe déjà (par nom)
            existing = await db.execute(
                select(Source).where(Source.name == src_data["name"])
            )
            if existing.scalar_one_or_none():
                print(f"  [SKIP] {src_data['name']}")
                skipped += 1
                continue

            source = Source(
                name=src_data["name"],
                organism=src_data["organism"],
                country=src_data["country"],
                source_type=src_data.get("source_type", "autre"),
                category=src_data.get("category", "public"),
                level=src_data.get("level", 2),
                reliability=src_data.get("reliability", 3),
                url=src_data["url"],
                collection_mode=src_data["collection_mode"],
                check_frequency=src_data.get("check_frequency", "weekly"),
                config=src_data.get("config", {}),
                is_active=src_data.get("is_active", False),
                notes=src_data.get("notes"),
                consecutive_errors=0,
            )
            db.add(source)
            print(f"  [ADD]  {src_data['name']} ({src_data['country']}, {src_data['category']})")
            added += 1

        await db.commit()
        print(f"\n✓ Terminé : {added} sources ajoutées, {skipped} déjà existantes.")
        print(f"  Total nouvelles sources : {added}")


if __name__ == "__main__":
    asyncio.run(run())
