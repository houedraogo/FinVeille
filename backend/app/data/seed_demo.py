async def seed_demo():
    print("⏳ Vérification des tables...")
    await create_tables()

    async with AsyncSessionLocal() as db:
        # Récupérer une source existante pour l'associer
        result = await db.execute(select(Source).limit(1))
        default_source = result.scalar_one_or_none()
        source_id = default_source.id if default_source else None

        created = 0
        skipped = 0

        for data in DEMO_DEVICES:
            # Vérifier si déjà existant (par titre exact)
            existing = await db.execute(
                select(Device).where(Device.title == data["title"]).limit(1)
            )
            if existing.scalar_one_or_none():
                skipped += 1
                continue

            # Préparer le dispositif
            slug = await _unique_slug(db, data["title"])
            content = (data["title"] + (data.get("short_description") or ""))

            device = Device(
                title=data["title"],
                slug=slug,
                title_normalized=data["title"].lower(),
                organism=data.get("organism", ""),
                organism_type=data.get("organism_type"),
                country=data.get("country", ""),
                region=data.get("region"),
                zone=data.get("zone"),
                geographic_scope=data.get("geographic_scope"),
                device_type=data.get("device_type", "autre"),
                aid_nature=data.get("aid_nature"),
                sectors=data.get("sectors", []),
                beneficiaries=data.get("beneficiaries", []),
                short_description=data.get("short_description"),
                eligibility_criteria=data.get("eligibility_criteria"),
                eligible_expenses=data.get("eligible_expenses"),
                amount_min=data.get("amount_min"),
                amount_max=data.get("amount_max"),
                currency=data.get("currency", "EUR"),
                funding_rate=data.get("funding_rate"),
                open_date=data.get("open_date"),
                close_date=data.get("close_date"),
                is_recurring=data.get("is_recurring", False),
                recurrence_notes=data.get("recurrence_notes"),
                status=data.get("status", "open"),
                source_id=source_id,
                source_url=data.get("source_url", ""),
                source_hash=compute_content_hash(content),
                language="fr",
                keywords=data.get("keywords", []),
                tags=data.get("tags", []),
                confidence_score=data.get("confidence_score", 70),
                completeness_score=data.get("completeness_score", 70),
                relevance_score=data.get("relevance_score", 70),
                validation_status="auto_published",
                auto_summary=_make_summary(data),
            )
            db.add(device)
            created += 1

        await db.commit()
        print(f"✅ {created} dispositif(s) démo créé(s), {skipped} ignoré(s) (déjà existants)")
        print("\n📊 Résumé :")
        print(f"   • France : {sum(1 for d in DEMO_DEVICES if d['country'] == 'France')} dispositifs")
        print(f"   • Afrique : {sum(1 for d in DEMO_DEVICES if d['country'] != 'France')} dispositifs")
        print(f"\n🌐 Rechargez http://localhost:3000 pour voir les données !")


async def _unique_slug(db, title: str) -> str:
    base = generate_slug(title)
    slug = base
    i = 1
    while True:
        r = await db.execute(select(Device.id).where(Device.slug == slug).limit(1))
        if not r.scalar():
            return slug
        slug = f"{base}-{i}"
        i += 1


def _make_summary(d: dict) -> str:
    type_labels = {
        "subvention": "propose une subvention",
        "pret": "propose un prêt public",
        "avance_remboursable": "propose une avance remboursable",
        "garantie": "propose une garantie",
        "credit_impot": "permet de bénéficier d'un crédit d'impôt",
        "exoneration": "prévoit des exonérations",
        "aap": "lance un appel à projets",
        "ami": "lance un appel à manifestation d'intérêt",
        "accompagnement": "propose un programme d'accompagnement",
        "concours": "organise un concours",
        "autre": "propose un dispositif de financement",
    }
    parts = [d.get("organism", "")]
    parts.append(type_labels.get(d.get("device_type", "autre"), "propose un financement"))
    if d.get("sectors") and d["sectors"] != ["transversal"]:
        parts.append(f"dans le secteur {', '.join(d['sectors'][:2])}")
    if d.get("amount_max"):
        parts.append(f"jusqu'à {d['amount_max']:,.0f} {d.get('currency', 'EUR')}".replace(",", " "))
    if d.get("close_date"):
        parts.append(f"— clôture le {d['close_date'].strftime('%d/%m/%Y')}")
    elif d.get("is_recurring"):
        parts.append("— dispositif récurrent")
    s = " ".join(parts)
    return s[0].upper() + s[1:] + "." if s else ""


